const buildItems = (
  slug: string,
  signature: string,
  titles: string[],
  kind: SantCategoryKey
) =>
  titles.map((title, index) => ({
    id: `${slug}-${kind}-${index + 1}`,
    title:
      slug === "tukaram" && kind === "abhang" && tukaramAbhangTitles[index]
        ? tukaramAbhangTitles[index]
        : title,
    content:
      kind === "abhang"
        ? makeAbhang(signature, index)
        : kind === "ovi"
          ? makeOvi(signature, index)
          : kind === "gaatha"
            ? makeGaatha(signature, index)
            : makeGranth(signature, index),
  }));

